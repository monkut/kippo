def strtobool(val: str | int | bool) -> bool:
    """Convert str/int value to bool"""
    if isinstance(val, bool):
        return val
    if isinstance(val, str):
        return val.lower() in ["true", "1", "t", "y", "yes"]
    if isinstance(val, int):
        return val == 1
    return bool(val)


